public class EjemploWhile {

    public static void main(String[] args) {
        /* Mientras (La expresión boolean sea vedadera
            Se repetira el bloque*/

        int x = 0;
        while (x < 100) {
            System.out.println(x);
            x++;
        }
        System.out.println("Fin de la ejecución");
    }

}
