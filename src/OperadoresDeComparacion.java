public class OperadoresDeComparacion {

    public static void main(String[] args) {
        System.out.println();

        double sueldo1= 10000.515558;
        double sueldo2=450055.052558;

        System.out.println(sueldo1>sueldo2);
        System.out.println(sueldo1<sueldo2);
        System.out.println(sueldo1>=sueldo2);
        System.out.println(sueldo1<=sueldo2);
        System.out.println(sueldo1!=sueldo2);
        System.out.println(sueldo1==sueldo2);

    }
}
